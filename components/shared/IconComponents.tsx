interface TooltipProps {
  children: React.ReactNode;
  text: string;
  position?: 'top' | 'bottom' | 'left' | 'right';
}

export const Tooltip: React.FC<TooltipProps> = ({ children, text, position = 'top' }) => {
  const positionClasses = {
    top: 'bottom-full left-1/2 -translate-x-1/2 mb-2',
    bottom: 'top-full left-1/2 -translate-x-1/2 mt-2',
    left: 'right-full top-1/2 -translate-y-1/2 mr-2',
    right: 'left-full top-1/2 -translate-y-1/2 ml-2',
  };

  const originClass = {
      top: 'origin-bottom',
      bottom: 'origin-top',
      left: 'origin-right',
      right: 'origin-left',
  }

  return (
    <div className="relative group flex items-center">
      {children}
      <div
        role="tooltip"
        className={`absolute ${positionClasses[position]} w-max whitespace-nowrap scale-0 group-hover:scale-100 transition-transform duration-150 ${originClass[position]} bg-slate-800 text-white text-xs font-semibold rounded-md px-2 py-1 z-50`}
      >
        {text}
      </div>
    </div>
  );
};